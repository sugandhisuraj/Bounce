import {action, makeAutoObservable, observable, computed} from 'mobx';
import {Placeholder} from '../../../assets';
class ScanTickets {
  @observable currentUser = {};
  constructor() {
    makeAutoObservable(this);
  }

  @action
  setCurrentUser = scanData => {
    this.currentUser = scanData;
  };

  @computed
  isCurrentUserEmpty = () => {
    return Object.keys(this.currentUser).length == 0;
  };

  @computed
  getPurchasedTicketsCount = () => {
    let totalQuantity = 0;
    this.currentUser?.party?.buyTickets?.map(bTicket => {
      totalQuantity += bTicket.selectedQuantity;
    });
    return totalQuantity;
  };
  @computed
  getCurrentScanDetails = () => {
    let userAvatar = Placeholder;
    if (this.currentUser?.user?.profileImage?.filePath) {
      userAvatar = {uri: this.currentUser?.user?.profileImage?.filePath};
    }
    let isPartyFree = this.currentUser?.party?.tickets?.length == 0 ?? 0;
    let purchasedTickets = null;
    let obj = {
      userAvatar,
      userName: this.currentUser.user.fullName,
      ticketLen: isPartyFree ? null : purchasedTickets,
      isAttending: true,
      //this.currentUser?.party?.isAttending ?? false
    };
    return obj;
  };
  static get instance() {
    return new ScanTickets();
  }
}

